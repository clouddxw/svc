package com.iresearch.svc.bean;

public class Item {
	private int brandrn;
    private int brandnum;
    private String items;
    private String imgurl;
	private int totunits;
	public int getBrandrn() {
		return brandrn;
	}
	public void setBrandrn(int brandrn) {
		this.brandrn = brandrn;
	}
	public int getBrandnum() {
		return brandnum;
	}
	public void setBrandnum(int brandnum) {
		this.brandnum = brandnum;
	}
	public String getItems() {
		return items;
	}
	public void setItems(String items) {
		this.items = items;
	}
	public String getImgurl() {
		return imgurl;
	}
	public void setImgurl(String imgurl) {
		this.imgurl = imgurl;
	}
	public int getTotunits() {
		return totunits;
	}
	public void setTotunits(int totunits) {
		this.totunits = totunits;
	}
	

}
